const ALL_TERMS = SECTIONS.flatMap(s => s.terms.map(t => ({ ...t, sectionId: s.id, sectionTitle: s.title })))

export default function WikiPage() {
  const [query, setQuery]     = useState('')
  const [section, setSection] = useState('all')
  const [open, setOpen]       = useState(null) // term.term

  const filtered = useMemo(() => {
    const q = query.toLowerCase().trim()
    return ALL_TERMS.filter(t => {
      const matchSection = section === 'all' || t.sectionId === section
      const matchQuery   = !q || [t.term, t.full, t.ru, t.def, t.example]
        .some(f => f.toLowerCase().includes(q))
      return matchSection && matchQuery
    })
  }, [query, section])

  const grouped = useMemo(() => {
    const map = new Map()
    SECTIONS.forEach(s => map.set(s.id, { ...s, terms: [] }))
    filtered.forEach(t => {
      if (map.has(t.sectionId)) map.get(t.sectionId).terms.push(t)
    })
    return [...map.values()].filter(s => s.terms.length > 0)
  }, [filtered])

  return (
    <div className="min-h-screen" style={{ background: 'var(--bg)', color: 'var(--text)' }}>
      <div className="max-w-[1100px] mx-auto px-8 py-10">

        {/* Header */}
        <div className="mb-8">
          <p className="text-[11px] font-bold tracking-[2px] uppercase text-amber-400 mb-3">Глоссарий</p>
          <h1 className="text-[clamp(28px,3.5vw,48px)] font-extrabold text-white tracking-[-1.5px] leading-[1.1] mb-4">
            Wiki — термины AlphaSignal
          </h1>
          <p className="text-[16px] text-muted max-w-[560px] leading-[1.7]">
            Все понятия, метрики и термины, которые встречаются при работе с платформой.
          </p>
        </div>

        {/* Search + filter */}
        <div className="flex gap-3 mb-8 flex-wrap">
          <input
            value={query}
            onChange={e => setQuery(e.target.value)}
            placeholder="Поиск термина... (например: MAPE, Sharpe, LSTM)"
            className="flex-1 min-w-[260px] bg-[var(--surface)] border border-[var(--border)] rounded-xl
                       px-4 py-2.5 text-[14px] text-warm placeholder:text-muted
                       focus:outline-none focus:border-amber-400/60 transition-colors"
          />
          <div className="flex gap-1 bg-[var(--surface)] border border-[var(--border)] rounded-xl p-1">
            {[['all', 'Все'], ...SECTIONS.map(s => [s.id, s.icon + ' ' + s.title.split(' ')[0]])].map(([key, label]) => (
              <button key={key} onClick={() => setSection(key)}
                className={`text-[12px] px-3 py-1.5 rounded-lg transition-all whitespace-nowrap
                  ${section === key
                    ? 'bg-amber-400/20 text-amber-400 border border-amber-400/30'
                    : 'text-muted hover:text-warm'}`}>
                {label}
              </button>
            ))}
          </div>
        </div>

        {/* Count */}
        {query && (
          <p className="text-[13px] text-muted mb-6">
            Найдено: <span className="text-warm font-semibold">{filtered.length}</span> терминов
          </p>
        )}

        {/* Terms */}
        <div className="space-y-10">
          {grouped.map(sec => (
            <div key={sec.id}>
              <div className="flex items-center gap-3 mb-4">
                <span className="text-xl">{sec.icon}</span>
                <h2 className="text-[18px] font-bold text-white">{sec.title}</h2>
                <span className="text-[12px] text-muted">({sec.terms.length})</span>
              </div>

              <div className="space-y-2">
                {sec.terms.map(t => {
                  const isOpen = open === t.term
                  return (
                    <div key={t.term}
                      className={`border rounded-xl transition-all duration-200 overflow-hidden
                        ${isOpen
                          ? 'border-amber-400/40 bg-amber-400/5'
                          : 'border-[var(--border)] bg-[var(--surface)] hover:border-amber-400/25'}`}>

                      {/* Term header */}
                      <button
                        onClick={() => setOpen(isOpen ? null : t.term)}
                        className="w-full flex items-center gap-4 px-5 py-4 text-left">
                        <div className="flex-1">
                          <div className="flex items-baseline gap-3 flex-wrap">
                            <span className={`text-[16px] font-bold ${isOpen ? 'text-amber-400' : 'text-white'}`}>
                              {t.term}
                            </span>
                            <span className="text-[12px] text-muted">{t.full}</span>
                          </div>
                          {!isOpen && (
                            <p className="text-[13px] text-muted mt-0.5 line-clamp-1">{t.def}</p>
                          )}
                        </div>
                        <span className={`text-muted transition-transform ${isOpen ? 'rotate-180' : ''}`}>▾</span>
                      </button>

                      {/* Expanded */}
                      {isOpen && (
                        <div className="px-5 pb-5 space-y-4">
                          {/* Russian name */}
                          {t.ru && (
                            <p className="text-[12px] text-amber-400/70 font-medium -mt-1">{t.ru}</p>
                          )}

                          {/* Definition */}
                          <p className="text-[14px] text-warm leading-[1.75]">{t.def}</p>

                          <div className="grid grid-cols-1 gap-3">
                            {/* Formula */}
                            <div className="bg-[var(--bg)] border border-[var(--border)] rounded-xl p-4">
                              <p className="text-[10px] font-bold uppercase tracking-[1.5px] text-muted mb-2">Формула / Определение</p>
                              <p className="text-[13px] text-amber-300 font-mono leading-[1.6]">{t.formula}</p>
                            </div>

                            {/* Example */}
                            <div className="bg-[var(--bg)] border border-[var(--border)] rounded-xl p-4">
                              <p className="text-[10px] font-bold uppercase tracking-[1.5px] text-muted mb-2">Пример</p>
                              <p className="text-[13px] text-warm leading-[1.6]">{t.example}</p>
                            </div>

                            {/* Range */}
                            <div className="bg-[var(--bg)] border border-amber-400/20 rounded-xl p-4">
                              <p className="text-[10px] font-bold uppercase tracking-[1.5px] text-amber-400 mb-2">Ориентиры / Нормы</p>
                              <p className="text-[13px] text-warm leading-[1.6]">{t.range}</p>
                            </div>
                          </div>
                        </div>
                      )}
                    </div>
                  )
                })}
              </div>
            </div>
          ))}

          {filtered.length === 0 && (
            <div className="text-center py-20 text-muted">
              <p className="text-4xl mb-4">🔍</p>
              <p className="text-[16px]">Ничего не найдено по запросу «{query}»</p>
              <button onClick={() => setQuery('')}
                className="mt-4 text-amber-400 text-[13px] hover:underline">
                Сбросить поиск
              </button>
            </div>
          )}
        </div>
      </div>
    </div>
  )
}
